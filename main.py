import pandas as pd

# Создание DataFrame с данными учеников
data = {
    'Ученик': ['Ученик 1', 'Ученик 2', 'Ученик 3', 'Ученик 4', 'Ученик 5',
               'Ученик 6', 'Ученик 7', 'Ученик 8', 'Ученик 9', 'Ученик 10'],
    'Математика': [8, 6, 9, 7, 8, 6, 7, 9, 7, 8],
    'Литература': [7, 8, 8, 9, 6, 7, 8, 9, 7, 8],
    'Физика': [9, 7, 6, 8, 9, 7, 9, 8, 6, 9],
    'Химия': [6, 7, 8, 7, 8, 6, 7, 9, 6, 8],
    'История': [8, 6, 9, 7, 8, 6, 7, 8, 7, 9]
}

df = pd.DataFrame(data)

# 1. Вывод первых строк DataFrame
print("Первые строки DataFrame:")
print(df.head())

# 2. Вычисление средней оценки по каждому предмету
average_scores = df[['Математика', 'Литература', 'Физика', 'Химия', 'История']].mean()
print("\nСредние оценки по предметам:")
print(average_scores)

# 3. Вычисление медианной оценки по каждому предмету
median_scores = df[['Математика', 'Литература', 'Физика', 'Химия', 'История']].median()
print("\nМедианные оценки по предметам:")
print(median_scores)

# 4. Вычисление Q1 и Q3 для оценок по математике
Q1_math = df['Математика'].quantile(0.25)
Q3_math = df['Математика'].quantile(0.75)
print(f"\nQ1 по математике: {Q1_math}")
print(f"Q3 по математике: {Q3_math}")

# 5. Рассчитайте IQR
IQR_math = Q3_math - Q1_math
print(f"IQR по математике: {IQR_math}")

# 6. Вычисление стандартного отклонения
std_deviation = df[['Математика', 'Литература', 'Физика', 'Химия', 'История']].std()
print("\nСтандартное отклонение по предметам:")
print(std_deviation)

